<br>มาตรา 18 (ห้ามโอน)
<br><br>
สมาชิกไม่สามารถโอนหรือมอบสิทธิ์ในการใช้บริการและสถานะตามสัญญาการใช้งานอื่นๆให้กับผู้อื่นและไม่สามารถให้เป็นการค้ำประกันได้
<br><br>
มาตรา 19 (ค่าเสียหาย)
<br><br>
บริษัทจะไม่รับผิดชอบต่อความเสียหายใดๆที่เกิดขึ้นกับสมาชิกที่เกี่ยวข้องกับการให้บริการฟรี ยกเว้นความเสียหายที่เกิดจากความประมาทเลินเล่อหรือความประมาทเลินเล่อที่สำคัญของบริษัท
<br><br>
มาตรา 20 (ข้อกำหนดการพ้นโทษ)
<br><br>
<br>① บริษัทจะได้รับการยกเว้นความรับผิดชอบในการให้บริการหากไม่สามารถให้บริการได้เนื่องจากภัยพิบัติทางธรรมชาติ สงคราม หรือแรงต้านอื่นๆที่เทียบเท่า
<br>② บริษัทจะได้รับการยกเว้นความรับผิดชอบต่อความเสียหายที่เกิดจากเหตุผลที่หลีกเลี่ยงไม่ได้ เช่น การซ่อมแซม การเปลี่ยนแปลง การตรวจสอบเป็นระยะๆ การก่อสร้างสิ่งอำนวยความสะดวกสำหรับบริการ
<br>③ บริษัทจะไม่รับผิดชอบต่ออุปสรรคในการใช้บริการเนื่องจากเหตุผลที่สมาชิกได้รับ
<br>④ บริษัทจะไม่รับผิดชอบต่อความเสียหายที่เกิดจากผลประโยชน์ที่สมาชิกคาดหวังจากการใช้บริการหรือข้อมูลที่ได้รับผ่านบริการ
<br>⑤ บริษัทจะไม่รับผิดชอบต่อเนื้อหา เช่น ข้อมูล ข้อมูล ข้อมูล ความน่าเชื่อถือของข้อเท็จจริง และความถูกต้องที่สมาชิกโพสต์ในบริการ
<br>มาตรา 21 (ศาลปกครอง)
<br>
ในกรณีที่มีการยื่นฟ้องเกี่ยวกับข้อพิพาทที่เกิดจากการใช้บริการ ศาลที่ดูแลสถานที่ตั้งของบริษัทจะเป็นศาลที่มีอำนาจพิเศษ
